    private boolean isValidDate(String dateStr) {
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    sdf.setLenient(false); // Không cho phép ngày không hợp lệ (ví dụ: 2023-02-30)
    try {
        sdf.parse(dateStr); // Kiểm tra định dạng
        return true;
    } catch (ParseException e) {
        return false;
    }
}
