    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String dateStr = dateField.getText();
        if (!isValidDate(dateStr)) {
            JOptionPane.showMessageDialog(RequestForm.this, "Định dạng ngày không hợp lệ! Vui lòng nhập ngày theo định dạng yyyy-MM-dd.");
            
        }
        ITRequest req = new ITRequest(0, nameField.getText(), Date.valueOf(dateField.getText()), emailField.getText(), typeField.getText(), detialField.getText());
        
        RequestRepository repo = new RequestRepository();
        try {
            repo.addRequest(req);
            System.out.println("Them thanh cong");
            JOptionPane.showMessageDialog(this, "Thêm yêu cầu thành công");

        } catch (IllegalArgumentException ex) {
            // Lỗi do ngày không đúng định dạng
            JOptionPane.showMessageDialog(this, "Định dạng ngày không hợp lệ! Vui lòng nhập ngày theo định dạng yyyy-MM-dd.");
        } catch (SQLException ex) {
            // Lỗi SQL khác
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm vào cơ sở dữ liệu: " + ex.getMessage());
        } catch (Exception ex) {
            // Lỗi không xác định
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + ex.getMessage());
        }


    }//GEN-LAST:event_addBtnActionPerformed

    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        // TODO add your handling code here:
        String id = idField.getText();
        RequestRepository repository = new RequestRepository();
        ITRequest req = new ITRequest();
        try {
            req = repository.findById(id);
            nameField.setText(req.getName());
            dateField.setText(req.getDate().toString());
            emailField.setText(req.getEmail());
            typeField.setText(req.getType());
            detialField.setText(req.getDetail());

        } catch (Exception ex) {
            System.out.println("Khong tim thay id " + id);
            JOptionPane.showMessageDialog(this, "khong tim thay id" + id);
        }
    }//GEN-LAST:event_findBtnActionPerformed

